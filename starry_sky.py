class Table:
    def __init__(self, data, names=None):
        if isinstance(data, dict):
            self.columns = data
            return
        if names is None:
            names = data[0]
            data = data[1:]
        self.columns = {}
        for name, column in zip(names, data.T):
            try:
                column = column.astype(int)
            except ValueError:
                try:
                    column = column.astype(float)
                except ValueError:
                    pass
            self.columns[name] = column

    def __getitem__(self, name):
        if isinstance(name, list):
            return Table({k: self.columns[k] for k in name})
        return self.columns[name]

    def __str__(self):
        rows = [",".join(self.columns)]
        for row in zip(*self.columns.values()):
            rows.append(",".join(map(str, row)))
        return "\n".join(rows)

    def __repr__(self):
        columns = self.columns
        n = len(next(iter(columns.values())))
        if len(columns) > 5:
            columns = chain(islice(columns, 4), ["..."])
        return f"<Table [{', '.join(columns)}] {n}rows>"

    @property
    def values(self):
        return np.array(list(self.columns.values())).T

    @staticmethod
    def read_csv(path, encoding=None, names=None):
        with path if isinstance(path, TextIOBase) else open(path, encoding=encoding) as fp:
            return Table(np.array([row for row in csv.reader(fp)]), names=names)


def make_material(obj):
    mat = bpy.data.materials.new(name="line")
    mat.use_nodes = True
    nd_pb = mat.node_tree.nodes["Principled BSDF"]
    nd_pb.inputs[27].default_value = 0.05, 0.05, 0.05, 1
    nd_pb.inputs[28].default_value = 1

    mat = bpy.data.materials.new(name="star")
    obj.active_material = mat
    mat.use_nodes = True
    nd_pb = mat.node_tree.nodes["Principled BSDF"]
    nd_a1 = mat.node_tree.nodes.new("ShaderNodeAttribute")
    nd_a1.attribute_name = "bv"
    nd_a1.attribute_type = "INSTANCER"
    nd_a2 = mat.node_tree.nodes.new("ShaderNodeAttribute")
    nd_a2.attribute_name = "lv"
    nd_a2.attribute_type = "INSTANCER"
    nd_cr = mat.node_tree.nodes.new("ShaderNodeValToRGB")
    nd_cr.color_ramp.color_mode = "HSV"
    nd_cr.color_ramp.hue_interpolation = "CCW"
    nd_cr.color_ramp.elements.new(0.95)
    nd_cr.color_ramp.elements[0].color = 0, 0.1, 1, 1
    nd_cr.color_ramp.elements[1].color = 1, 0, 0, 1
    nd_cr.color_ramp.elements[2].color = 0.01, 0.01, 0.01, 1
    nd_a1.location = -450, -180
    nd_a2.location = -450, 5
    nd_cr.location = -270, 5
    mat.node_tree.links.new(nd_a1.outputs[2], nd_cr.inputs[0])
    mat.node_tree.links.new(nd_cr.outputs[0], nd_pb.inputs[27])
    mat.node_tree.links.new(nd_a2.outputs[2], nd_pb.inputs[28])


def make_geometry_node(obj):
    modifier = obj.modifiers.new("GeometryNodes", "NODES")
    node_group = bpy.data.node_groups.new("Geometry Nodes", "GeometryNodeTree")
    modifier.node_group = node_group
    node_group.is_modifier = True
    node_group.interface.new_socket("Geometry", in_out="OUTPUT", socket_type="NodeSocketGeometry")
    node_group.interface.new_socket("Geometry", in_out="INPUT", socket_type="NodeSocketGeometry")
    nd_gi = node_group.nodes.new("NodeGroupInput")
    nd_ic = node_group.nodes.new("GeometryNodeMeshIcoSphere")
    nd_ic.inputs[0].default_value = 0.003
    nd_io = node_group.nodes.new("GeometryNodeInstanceOnPoints")
    nd_mc = node_group.nodes.new("GeometryNodeMeshToCurve")
    nd_cc = node_group.nodes.new("GeometryNodeCurvePrimitiveCircle")
    nd_cc.inputs[0].default_value = 6
    nd_cc.inputs[4].default_value = 0.001
    nd_cm = node_group.nodes.new("GeometryNodeCurveToMesh")
    nd_sm = node_group.nodes.new("GeometryNodeSetMaterial")
    nd_sm.inputs[2].default_value = bpy.data.materials["star"]
    nd_sm2 = node_group.nodes.new("GeometryNodeSetMaterial")
    nd_sm2.inputs[2].default_value = bpy.data.materials["line"]
    nd_jo = node_group.nodes.new("GeometryNodeJoinGeometry")
    nd_go = node_group.nodes.new("NodeGroupOutput")

    node_group.links.new(nd_gi.outputs[0], nd_io.inputs[0])
    node_group.links.new(nd_ic.outputs[0], nd_io.inputs[2])
    node_group.links.new(nd_gi.outputs[0], nd_mc.inputs[0])
    node_group.links.new(nd_mc.outputs[0], nd_cm.inputs[0])
    node_group.links.new(nd_cc.outputs[0], nd_cm.inputs[1])
    node_group.links.new(nd_io.outputs[0], nd_sm.inputs[0])
    node_group.links.new(nd_cm.outputs[0], nd_sm2.inputs[0])
    node_group.links.new(nd_sm.outputs[0], nd_jo.inputs[0])
    node_group.links.new(nd_sm2.outputs[0], nd_jo.inputs[0])
    node_group.links.new(nd_jo.outputs[0], nd_go.inputs[0])

    nds = [nd_gi, nd_ic, nd_io, nd_mc, nd_cc, nd_cm, nd_sm, nd_sm2, nd_jo, nd_go]
    locs = [[-315, 116], [-315, -18], [-151, 116], [8, 116], [8, -18]]
    locs += [[170, 116], [170, -18], [325, 116], [325, -18], [482, 116]]
    for nd, loc in zip(nds, locs):
        nd.location = loc


def make_object():
    df = Table.read_csv(StringIO(df_str))
    dfl = Table.read_csv(StringIO(dfl_str))
    verts = (df[["X", "Y", "Z"]].values / 10).tolist()
    edges = dfl[["ID1", "ID2"]].values.tolist()
    mesh = bpy.data.meshes.new(name="Star")
    mesh.from_pydata(verts, edges, [])
    obj = bpy.data.objects.new(mesh.name, mesh)
    bpy.context.scene.collection.objects.link(obj)
    mesh.attributes.new("lv", "FLOAT", "POINT")
    mesh.attributes["lv"].data.foreach_set("value", ((4 - df["V"]) ** 2 * 5).tolist())
    mn, mx = df["B"].min(), df["B"].max()
    mesh.attributes.new("bv", "FLOAT", "POINT")
    mesh.attributes["bv"].data.foreach_set("value", ((df["B"] - mn) / (mx - mn)).tolist())
    make_material(obj)
    make_geometry_node(obj)
    bpy.data.worlds["World"].node_tree.nodes["Background"].inputs[0].default_value = 0, 0, 0, 1
    set_bloom()


def set_bloom():
    scene = bpy.data.scenes["Scene"]
    scene.use_nodes = True
    nodes = scene.node_tree.nodes
    glare = nodes.new(type="CompositorNodeGlare")
    glare.glare_type = "BLOOM"
    glare.quality = "LOW"
    glare.location = 50, 200
    node1 = scene.node_tree.nodes["Render Layers"]
    node2 = scene.node_tree.nodes["Composite"]
    scene.node_tree.links.new(node1.outputs[0], glare.inputs[0])
    scene.node_tree.links.new(glare.outputs[0], node2.inputs[0])
    for area in bpy.data.screens["Layout"].areas:
        if area.ui_type == "VIEW_3D":
            area.spaces[0].shading.type = "RENDERED"
            area.spaces[0].shading.use_compositor = "ALWAYS"


